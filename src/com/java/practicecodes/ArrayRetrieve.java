public class ArrayRetrieve {
    public static void main(String[] args)
    {
        int[] arr = new int[]{12,34,43,54,65};
        System.out.println(arr);
        System.out.println(arr.length);
        for (int i =0;i<arr.length;i++)
        {
            System.out.println(arr[i]);
        }

    }
}
